/* === Testing Package ===
- Di bahasa pemorgraman lain, biasanya untuk implementasi unit test, kita butuh library atau framework
	khusus untuk unit test
- Berbeda dengan Go-Lang, di Go-Lang sudah untuk unit test sudah disediakan sebuah package khusus bernama
	testing
- Selain itu untuk menjalankan unit test, di Go-Lang juga sudah disediakan perintah nya
- Hal ini membuat implementasi unit testing di golang sangat mudah dibanding dengan bahasa pemorgraman
	yang lain
*/

/* === testing.T ===
- Go-Lang menyediakan sebuah struct yang bernama testing.T
- Struct ini digunakan untuk unit test di Go-Lang
*/

/* === testing.M ===
- testing.M adalah struct yang disediakan Go-Lang untuk mengatur life cycle testing
- Materi ini nanti akan dibahas di chapter Main
*/

/* === testing.B ===
- testing.B adalah struct yang disediakan Go-Lang untuk melakukan benchmarking
- Di Go-Lang untuk melakukan benchmark (mengukur kecepatan kode program) pun sudah disediakan, sehingga
	kita tidak perlu menggunakan library atau framework tambahan
*/